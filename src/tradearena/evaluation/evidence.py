from __future__ import annotations

from collections.abc import Iterable

ALLOWED_EVIDENCE_TAGS = (
    "stress-only",
    "cached-provider",
    "live-provider",
    "deterministic-baseline",
    "external-submitted",
    "quote-calibrated",
    "fill-replay-validated",
    "redacted-prompt",
    "fully-auditable",
)

EVIDENCE_TAG_DESCRIPTIONS = {
    "stress-only": "Execution uses shared stress assumptions, not venue-calibrated transaction-cost prediction.",
    "cached-provider": "Provider-backed behavior is replayed or published through cache/redacted manifest evidence.",
    "live-provider": "Provider-backed behavior came from a live API call in the declared run.",
    "deterministic-baseline": "Policy is deterministic or seeded local code, not an LLM provider.",
    "external-submitted": "Row was submitted by an external participant rather than generated by the maintainer script.",
    "quote-calibrated": "Execution parameters are fitted from top-of-book quotes plus realized fills.",
    "fill-replay-validated": "Orders were replayed against realized fill logs.",
    "redacted-prompt": "Raw provider prompt/response text is not public; a redacted manifest is provided.",
    "fully-auditable": "Raw decision, risk, execution, and provenance artifacts are public enough for full replay.",
}


def evidence_tags_for_row(
    *,
    provider: str,
    execution_mode: str = "realistic-stress",
    provider_mode: str = "cached",
    raw_provider_text_removed: bool = True,
    trajectory_format: str = "redacted_manifest",
    external_submitted: bool = False,
) -> list[str]:
    tags: list[str] = []
    normalized_provider = provider.lower()
    normalized_execution = execution_mode.lower()
    normalized_provider_mode = provider_mode.lower()

    if normalized_execution in {"realistic", "realistic-stress", "stress"}:
        tags.append("stress-only")
    elif normalized_execution in {"calibrated", "calibrated-realistic", "quote-replay"}:
        tags.append("quote-calibrated")
    elif normalized_execution in {"fill-replay", "quote-fill-replay"}:
        tags.append("quote-calibrated")
        tags.append("fill-replay-validated")

    if normalized_provider in {"baseline", "deterministic"}:
        tags.append("deterministic-baseline")
    else:
        tags.append("live-provider" if normalized_provider_mode == "live" else "cached-provider")
        if raw_provider_text_removed:
            tags.append("redacted-prompt")

    if external_submitted:
        tags.append("external-submitted")

    if trajectory_format == "json" and not raw_provider_text_removed:
        tags.append("fully-auditable")

    return _dedupe_allowed(tags)


def claim_scope_for_tags(tags: Iterable[str]) -> str:
    tag_set = set(tags)
    if "fill-replay-validated" in tag_set:
        return "fill-replay execution validation"
    if "quote-calibrated" in tag_set:
        return "quote/fill calibrated execution evidence"
    if "live-provider" in tag_set and "stress-only" in tag_set:
        return "live-provider behavior under stress-only execution"
    if "cached-provider" in tag_set and "stress-only" in tag_set:
        return "cached-provider reliability under stress-only execution"
    if "deterministic-baseline" in tag_set and "stress-only" in tag_set:
        return "deterministic baseline under stress-only execution"
    return "auditable benchmark evidence"


def evidence_payload(tags: Iterable[str]) -> dict[str, object]:
    normalized = _dedupe_allowed(tags)
    return {
        "tags": normalized,
        "claim_scope": claim_scope_for_tags(normalized),
        "tag_definitions": {tag: EVIDENCE_TAG_DESCRIPTIONS[tag] for tag in normalized},
    }


def evidence_payload_for_row(
    *,
    provider: str,
    execution_mode: str = "realistic-stress",
    provider_mode: str = "cached",
    raw_provider_text_removed: bool = True,
    trajectory_format: str = "redacted_manifest",
    external_submitted: bool = False,
) -> dict[str, object]:
    return evidence_payload(
        evidence_tags_for_row(
            provider=provider,
            execution_mode=execution_mode,
            provider_mode=provider_mode,
            raw_provider_text_removed=raw_provider_text_removed,
            trajectory_format=trajectory_format,
            external_submitted=external_submitted,
        )
    )


def validate_evidence_tags(tags: Iterable[object]) -> list[str]:
    allowed = set(ALLOWED_EVIDENCE_TAGS)
    errors = []
    for tag in tags:
        if not isinstance(tag, str):
            errors.append("evidence.tags entries must be strings")
        elif tag not in allowed:
            errors.append(f"unsupported evidence tag: {tag}")
    return errors


def format_evidence_tags(tags: Iterable[object]) -> str:
    return ";".join(str(tag) for tag in tags if str(tag))


def parse_evidence_tags(value: object) -> list[str]:
    if isinstance(value, list):
        return [str(item) for item in value if str(item)]
    return [part.strip() for part in str(value or "").split(";") if part.strip()]


def _dedupe_allowed(tags: Iterable[str]) -> list[str]:
    allowed = set(ALLOWED_EVIDENCE_TAGS)
    result = []
    seen = set()
    for tag in tags:
        if tag not in allowed or tag in seen:
            continue
        seen.add(tag)
        result.append(tag)
    return result
